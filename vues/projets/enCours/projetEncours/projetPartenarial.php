                                    <h2>Description du Projet</h2>
                                    <p>ACTIONS PREPAS GABON lie avec la ville d’Asnières-sur-Seine (92, France) un partenariat basé sur une charte associative. Pour donc remplir sa  part de contrat, ACTIONS PREPAS GABON a lancé trois projets essentiellement éducatif, ludique et social pour participer à la vie associative de la ville...<br/><br/>
									   L’organisation d’une Kermesse en avril 2011 et le mini championnat inter associations de la ville, sont au-delà de leur aspect social, les deux projets à forte connotations ludique et éducatif. Le troisième projet, et qui n’est pas des moindres, repose sur la mise en place d’actions bénévoles pour venir en aide, en usant de nos compétences aux personnes qui en éprouvent le besoin.<br/><br/>
									   Les trois projets, et encore plus le troisième, nécessite une synchronisation et une cohésion en amont, entre les deux parties que sont la ville d’Asnières-sur-Seine et ACTIONS PREPAS GABON avant leur réalisation. Une proposition de cours de soutien aux élèves en difficultés serait éventuellement l’une des actions bénévoles que la structure pourrait mener dans ce troisième projet.
